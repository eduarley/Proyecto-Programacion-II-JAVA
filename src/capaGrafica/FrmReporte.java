    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String ID=JOptionPane.showInputDialog(
        null, 
        "Ingrese el ID del salonero a buscar", 
        "Buscar pago de saloneros", 
        JOptionPane.WARNING_MESSAGE);
        
          
        
        
        
        if(ArchivadorUsuario.buscarUsuario(ID)!=null){
            try {
                //JOptionPane.showMessageDialog(null, "Total propina: "+Cuenta.sumaPropina(ID));
                ArrayList<Cuenta> arrayCuentaT=Cuenta.listadoCuentas();
                double propina=0;
                for (Cuenta cuentaT : arrayCuentaT) {
                    if(cuentaT.getSalonero().getCedula().equalsIgnoreCase(ID)){
                    propina+=cuentaT.impuestoServicio();
                }
                    
                }
                JOptionPane.showMessageDialog(null, "Total propina: "+propina);
            } catch (Exception ex) {
                Logger.getLogger(FrmReporte.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "No existe usuario con la c√©dula: "+ID,"Error al buscar el usuario",JOptionPane.ERROR_MESSAGE);
        }
    
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        double totalDecuentas=0;
        try {
            ArrayList<Cuenta> arrayCuentaT=Cuenta.listadoCuentas();
            for (Cuenta cuenta : arrayCuentaT) {

                totalDecuentas+=cuenta.total();
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmReporte.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(null, "Total de las cuentas: "+totalDecuentas+ " colones");
    }//GEN-LAST:event_jButton4ActionPerformed


